function drawComparisonTable(doc, evaluations, pageState) {
  let y = MARGIN;
  doc.font('Helvetica-Bold').fontSize(14).fillColor(NAVY);
  doc.text('Cross-School Comparison', MARGIN, y, { width: CONTENT_W });
  y += 24;

  const schools = evaluations.map((e) => e.university || 'School');
  const dimColW = 118;
  const n = schools.length;
  const cellW = (CONTENT_W - dimColW) / n;
  const rowH = 22;
  const headerH = 28;

  y = ensureSpace(doc, y, headerH + rowH * 7 + 8, pageState);

  let x = MARGIN;
  doc.save();
  doc.font('Helvetica-Bold').fontSize(8).fillColor(NAVY);
  cellRect(doc, x, y, dimColW, headerH, ROW_GRAY);
  doc.fillColor(NAVY);
  doc.text('Dimension', x + 4, y + 8, { width: dimColW - 8 });
  x += dimColW;
  for (let i = 0; i < n; i++) {
    const alt = i % 2 === 0 ? ROW_GRAY : '#ffffff';
    cellRect(doc, x, y, cellW, headerH, alt);
    doc.fillColor(NAVY);
    const name = schools[i].length > 22 ? `${schools[i].slice(0, 20)}…` : schools[i];
    doc.font('Helvetica-Bold').fontSize(7);
    doc.text(name, x + 3, y + 6, { width: cellW - 6, align: 'center' });
    x += cellW;
  }
  doc.restore();
  y += headerH;

  const rows = [...DIMENSION_ROWS.map((d) => ({ label: d.label, field: d.field, bold: false })), { label: 'Alignment Score', field: 'alignmentScore', bold: true }];

  let rowIdx = 0;
  for (const row of rows) {
    y = ensureSpace(doc, y, rowH + 4, pageState);
    x = MARGIN;
    const bg = rowIdx % 2 === 0 ? '#ffffff' : ROW_GRAY;
    cellRect(doc, x, y, dimColW, rowH, bg);
    doc.fillColor(NAVY);
    doc.font(row.bold ? 'Helvetica-Bold' : 'Helvetica').fontSize(8);
    doc.text(row.label, x + 4, y + 6, { width: dimColW - 8 });
    x += dimColW;
    for (let i = 0; i < n; i++) {
      const ev = evaluations[i];
      const score = typeof ev[row.field] === 'number' ? ev[row.field] : 0;
      drawScoreFillRect(doc, x, y, cellW, rowH, score);
      doc.rect(x, y, cellW, rowH).strokeColor('#e5e7eb').lineWidth(0.3).stroke();
      doc.font(row.bold ? 'Helvetica-Bold' : 'Helvetica').fontSize(9).fillColor(scoreTextColor(score));
      doc.text(formatScore(score), x, y + 6, { width: cellW, align: 'center' });
      x += cellW;
    }
    y += rowH;
    rowIdx += 1;
  }

  y += 16;
  doc.font('Helvetica-Bold').fontSize(11).fillColor(NAVY);
  doc.text('Patterns', MARGIN, y);
  y += 14;
  doc.font('Helvetica').fontSize(10).fillColor(NAVY);
  for (const b of patternsBullets(evaluations)) {
    y = ensureSpace(doc, y, 28, pageState);
    doc.fillColor(NAVY).circle(MARGIN + 3, y + 4, 1.5).fill();
    doc.fillColor(NAVY).font('Helvetica').fontSize(10);
    doc.text(b, MARGIN + 12, y, { width: CONTENT_W - 12, align: 'left' });
    y += doc.heightOfString(b, { width: CONTENT_W - 12 }) + 6;
  }
}

function drawPage3(doc, data, pageState) {
  let y = MARGIN;
  doc.font('Helvetica-Bold').fontSize(14).fillColor(NAVY);
  doc.text('Strategic Priorities', MARGIN, y, { width: CONTENT_W });
  y += 26;

  const evaluations = data.evaluations || [];
  const avgs = dimensionAverages(evaluations);
  const sortedGaps = Object.entries(avgs).sort((a, b) => a[1] - b[1]);
  const top3 = sortedGaps.slice(0, 3);

  for (const [insight, avg] of top3) {
    y = ensureSpace(doc, y, 70, pageState);
    doc.font('Helvetica-Bold').fontSize(11).fillColor(NAVY);
    doc.text(`${insightLabel(insight)} — avg ${formatScore(avg)}/10`, MARGIN, y);
    y += 14;
    doc.font('Helvetica').fontSize(10).fillColor(NAVY);
    doc.text(`Impact: ${impactLevel(avg)}`, MARGIN, y);
    y += 14;
    const weak = topWeaknessForInsight(evaluations, insight);
    const weakLine = `Top weakness: ${weak}`;
    doc.text(weakLine, MARGIN, y, { width: CONTENT_W });
    y += doc.heightOfString(weakLine, { width: CONTENT_W }) + 12;
  }

  y += 8;
  const strongDims = Object.entries(avgs).filter(([, v]) => v >= 7.0);
  if (strongDims.length > 0) {
    y = ensureSpace(doc, y, 40, pageState);
    doc.font('Helvetica-Bold').fontSize(12).fillColor(NAVY);
    doc.text('Already Strong — Protect These', MARGIN, y);
    y += 16;
    doc.font('Helvetica').fontSize(10).fillColor(TEAL);
    for (const [k, v] of strongDims) {
      y = ensureSpace(doc, y, 16, pageState);
      doc.text(`${insightLabel(k)}: ${formatScore(v)}/10 avg`, MARGIN, y, { width: CONTENT_W });
      y += 14;
    }
  }

  const essays = data.essayAnalyses || [];
  if (essays.length > 0) {
    y += 10;
    y = ensureSpace(doc, y, 36, pageState);
    doc.font('Helvetica-Bold').fontSize(12).fillColor(NAVY);
    doc.text('Essay Performance by School', MARGIN, y);
    y += 18;

    const colSchool = 130;
    const colNum = 52;
    const tableW = CONTENT_W;
    const rowH = 20;
    y = ensureSpace(doc, y, rowH + 8, pageState);
    let x = MARGIN;
    doc.save();
    doc.font('Helvetica-Bold').fontSize(7).fillColor(NAVY);
    cellRect(doc, x, y, colSchool, rowH, ROW_GRAY);
    doc.text('School', x + 3, y + 6, { width: colSchool - 6 });
    x += colSchool;
    for (const h of ['Strategic', 'Content', 'Structure', 'Verdict']) {
      const w = h === 'Verdict' ? tableW - colSchool - 3 * colNum : colNum;
      cellRect(doc, x, y, w, rowH, ROW_GRAY);
      doc.text(h, x + 2, y + 6, { width: w - 4, align: 'center' });
      x += w;
    }
    doc.restore();
    y += rowH;

    for (const ea of essays) {
      const verdict = String(ea.overallVerdict || '').slice(0, 100);
      y = ensureSpace(doc, y, rowH + 4, pageState);
      x = MARGIN;
      doc.font('Helvetica').fontSize(7).fillColor(NAVY);
      cellRect(doc, x, y, colSchool, rowH, '#ffffff');
      doc.text((ea.university || '—').slice(0, 28), x + 3, y + 5, { width: colSchool - 6 });
      x += colSchool;
      const nums = [
        typeof ea.strategicFit === 'number' ? formatScore(ea.strategicFit) : '—',
        typeof ea.contentAnalysis === 'number' ? formatScore(ea.contentAnalysis) : '—',
        typeof ea.structureAndVoice === 'number' ? formatScore(ea.structureAndVoice) : '—',
      ];
      for (const num of nums) {
        cellRect(doc, x, y, colNum, rowH, '#ffffff');
        doc.text(num, x, y + 6, { width: colNum, align: 'center' });
        x += colNum;
      }
      const vw = tableW - colSchool - 3 * colNum;
      cellRect(doc, x, y, vw, rowH, '#ffffff');
      doc.text(verdict || '—', x + 3, y + 4, { width: vw - 6 });
      y += rowH;
    }
  }
}

