    y = ensureSpace(doc, y, 56, pageState);
    doc.font('Helvetica-Bold').fontSize(11).fillColor(NAVY);
    doc.text(`${idx + 1}. ${item.question || ''}`, MARGIN, y, { width: CONTENT_W });
